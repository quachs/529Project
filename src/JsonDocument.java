/*
 * Class to represent a Json file
 */

public class JsonDocument {
    private String title;
    private String body;
    private String url;
    private String author;
    
    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @return the body
     */
    public String getBody() {
        return body;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @return the author
     */
    public String getAuthor() {
        return author;
    }    
    
}